int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int ifs;

	ifs = 0;
	i = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) == 1)
		{
			ifs++;
		}
		i++;
	}
	return (ifs);
}
